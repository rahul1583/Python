#operators in python
#1 Arithmetic Operators +, -, *, /, %, **, //
#2 Assignment Operators =, +=, -=, *=, /=, %=, **=, //=,
#3 Comparison Operators ==, !=, >, <, >=, <=
#4 Logical Operators and, or, not
#5 Identity Operators is, is not
#6 Membership Operators in, not in
#7 Bitwise Operators &, |, ^, ~, <<, >>
#8 Ternary Operator (Conditional Expression) a if condition else b
num1 = 10
num2 = 20

sum = num1 + num2
print(f"This is sum {sum}")

pow = num1**2
print(pow)

# Comparison Operators
print(num1 > num2)
print(num1 < num2 and num1 > 5)

print(num1>=num2 or num1<=num2)

# Logical Operators
print(num1 == num2)
print(num1 != num2)
print(num1 is num2)
